display_line(Board,LineNumber) :-
    display_row_letter(LineNumber),
    get_board_value(Board,LineNumber,0,L1),
    display_element(L1),
    write('  ||  '),
    get_board_value(Board,LineNumber,1,L2),
    display_element(L2),
    write('  ||  '),
    get_board_value(Board,LineNumber,2,L3),
    display_element(L3),
    write('  ||  '),
    get_board_value(Board,LineNumber,3,L4),
    display_element(L4),
    write('  ||  '),
    get_board_value(Board,LineNumber,4,L5),
    display_element(L5),
    write('  ||  '),
    get_board_value(Board,LineNumber,5,L6),
    display_element(L6),
    write('  |||').

